package pack2;

public class IfAndElse {
	
	
	public static void main(String[] args) {
		
		int x=2;
		
		
		if(x==10) {
			System.out.println("I am human");
		}
		else if(x==20) {
			System.out.println("I am not human");
		}
		else if(x==30) {
			System.out.println("I am robot");
		}
		
		else {
			System.out.println("Invalid number");
		}
		
		
		
	}//end of main method

}//end of class
